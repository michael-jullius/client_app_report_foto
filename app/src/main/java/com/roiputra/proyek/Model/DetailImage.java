package com.roiputra.proyek.Model;

public class DetailImage {

    private String tukang, assisten_tukang, alokasi_pekerjaan, cuaca, volume, keterangan, nama_user;

    public String getTukang() {
        return tukang;
    }

    public void setTukang(String tukang) {
        this.tukang = tukang;
    }

    public String getAssisten_tukang() {
        return assisten_tukang;
    }

    public void setAssisten_tukang(String assisten_tukang) {
        this.assisten_tukang = assisten_tukang;
    }

    public String getAlokasi_pekerjaan() {
        return alokasi_pekerjaan;
    }

    public void setAlokasi_pekerjaan(String alokasi_pekerjaan) {
        this.alokasi_pekerjaan = alokasi_pekerjaan;
    }

    public String getCuaca() {
        return cuaca;
    }

    public void setCuaca(String cuaca) {
        this.cuaca = cuaca;
    }

    public String getVolume() {
        return volume;
    }

    public void setVolume(String volume) {
        this.volume = volume;
    }


    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getNama_user() {
        return nama_user;
    }

    public void setNama_user(String nama_user) {
        this.nama_user = nama_user;
    }
}
